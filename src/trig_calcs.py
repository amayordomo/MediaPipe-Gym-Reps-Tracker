import numpy as np      # helps with trig 

def calculate_angle(a,b,c):
    a = np.array(a)
    b = np.array(b)
    c = np.array(c)

    radians = np.arctan2(c[1]-b[1], c[0]-b[0]) - np.arctan2(a[1]-b[1], a[0]-b[0])
    angle = np.abs(radians*180.0/np.pi)
    
    # max angle of arm is 180, not 260
    if angle >180.0:
        angle = 360-angle
        
    return angle 